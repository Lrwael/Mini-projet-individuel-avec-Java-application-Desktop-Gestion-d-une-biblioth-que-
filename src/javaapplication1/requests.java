    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        try {
    String sql = "SELECT status FROM request WHERE req_ID = ?;";
    PreparedStatement st = con.prepareStatement(sql);
    st.setString(1, rid.getText());
    ResultSet rs = st.executeQuery();
    if (rs.next()) {
        String status = rs.getString("status");
        if (status.equals("Rejected")) {
            JOptionPane.showMessageDialog(null, "Request already rejected, cannot reject it again!", "Error", JOptionPane.ERROR_MESSAGE);
        } else if (status.equals("Approved")) {
            JOptionPane.showMessageDialog(null, "Request already approved, cannot reject it!", "Error", JOptionPane.ERROR_MESSAGE);
        } else if (status.equals("Pending")) {
            String sql2 = "UPDATE request SET status = 'Rejected' WHERE req_ID = ?;";
            PreparedStatement st2 = con.prepareStatement(sql2);
            st2.setString(1, rid.getText());
            st2.executeUpdate();
            JOptionPane.showMessageDialog(null, "Request n°:" + rid.getText() + " rejected.", "Success", JOptionPane.OK_OPTION);
            setVisible(false);
            requests ob = new requests();
            ob.setVisible(true);
        } else {
            JOptionPane.showMessageDialog(null, "Invalid request status!", "Error", JOptionPane.ERROR_MESSAGE);
        }
    } else {
        JOptionPane.showMessageDialog(null, "Request not found!", "Error", JOptionPane.ERROR_MESSAGE);
    }
} catch (SQLException ex) {
    Logger.getLogger(requests.class.getName()).log(Level.SEVERE, null, ex);
}



    }//GEN-LAST:event_jButton2ActionPerformed


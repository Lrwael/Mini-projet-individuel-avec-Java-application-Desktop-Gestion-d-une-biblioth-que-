    private void ApproveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ApproveActionPerformed
        try {
    String sql = "SELECT doc_ID,status FROM request WHERE req_ID=?;";
    PreparedStatement st = con.prepareStatement(sql);
    st.setString(1, rid.getText());
    ResultSet rs = st.executeQuery();
    if (rs.next()) {
        String status = rs.getString("status");
        if(status.equals("Approved")){
            JOptionPane.showMessageDialog(null, "Request already approved, cannot approved it again !", "Error", JOptionPane.ERROR_MESSAGE);
        }
        else if (status.equals("Rejected")){
            JOptionPane.showMessageDialog(null, "Request already rejected, cannot reject it !", "Error", JOptionPane.ERROR_MESSAGE);
        }else{
        String pid = rs.getString(1);

        String sql1 = "UPDATE request SET status='Approved' WHERE req_ID=?";
        PreparedStatement st1 = con.prepareStatement(sql1);
        st1.setString(1, rid.getText());
        st1.executeUpdate();

        String sql2 = "DELETE FROM book WHERE doc_ID=?";
        PreparedStatement st2 = con.prepareStatement(sql2);
        st2.setString(1, pid);
        st2.executeUpdate();

        String sql3 = "DELETE FROM document WHERE doc_ID=?";
        PreparedStatement st3 = con.prepareStatement(sql3);
        st3.setString(1, pid);
        st3.executeUpdate();
        
        JOptionPane.showMessageDialog(null, "Request n°:" + rid.getText() + " Approved.", "Success", JOptionPane.INFORMATION_MESSAGE);
        setVisible(false);
        requests ob=new requests();
        ob.setVisible(true);
        }
    } else {
        JOptionPane.showMessageDialog(null, "Invalid choice !", "Error", JOptionPane.ERROR_MESSAGE);
    }
} catch (SQLException ex) {
    Logger.getLogger(requests.class.getName()).log(Level.SEVERE, null, ex);
}


    }//GEN-LAST:event_ApproveActionPerformed

